def my_first_interpreter(code):
    cell = 0
    res = []
    for c in code:
        if c not in ['+', '.']:
            continue
        if c == '+':
            cell += 1
        else:
            print(int(cell & 0xff))
            res.append(chr(cell & 0xff))

    return ''.join(res)


